import java.util.Scanner

fun main(args: Array<String>) {
    val scanner = Scanner(System.`in`)

    val num1 = scanner.nextLine() // reads the first number
    val num2 = scanner.nextLine() // reads the second number
    val num3 = scanner.nextInt() // reads the second number
    val num4 = scanner.nextInt() // reads the second number
    println(num1) // prints the first number
    println(num2) // prints the second number
    println(num3) // prints the second number
    println(num4) // prints the second number
}